package ex_09_Switch;

public class Lab096_Interview
{
    public static void main(String[] args) {
        char code='c';
        switch(code)
        {
            default:
                System.out.println("helooo");
            case 'a':
                System.out.println("65");
                break;
            case 'b':
                System.out.println("66");
                break;
        }
    }
}
