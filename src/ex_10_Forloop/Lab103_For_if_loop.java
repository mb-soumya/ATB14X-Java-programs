package ex_10_Forloop;

public class Lab103_For_if_loop
{
    public static void main(String[] args) {
        for (int shruti = 0; shruti < 18; shruti++) {
            if (shruti > 15)
            {
                System.out.println("get gift from papa, iphone");
            }
            else {
                System.out.println("no gift");
            }

        }
    }
}
